    private Boolean logic_time_error()
    {   

        if ((int)weekly_from_hours.getValue() > (int)weekly_to_hours.getValue())
        {
              return true;
        } 
       
         if ((int)weekly_to_hours.getValue() == (int)weekly_from_hours.getValue())
        {
            if ((int)weekly_from_minutes.getValue() >= (int)weekly_to_minutes.getValue())
            {
                return true;
            }
        } 
         return false;
    }
    
    
